package com.fzc.json;

import java.util.Set;

public class Student {
	private String name ;
	private Set books;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Set getBooks() {
		return books;
	}
	public void setBooks(Set books) {
		this.books = books;
	}
}
